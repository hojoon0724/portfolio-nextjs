export function sixLetters(text) {
  const str = text;
  return str.slice(0, 6);
}

export function orderArray(array) {
  let trimmed = array.map(sixLetters);
  const ordered = trimmed.sort();

  let unique = [];
  let i = 0;
  for (i = 0; i < ordered.length; i++) {
    const j = i + 1;
    if (ordered[i] !== ordered[j]) {
      unique.push(ordered[i]);
    }
  }
  return unique;
}

export function convertToHSL(hex) {
  {
    const rHex = hex.slice(0, 2);
    const gHex = hex.slice(2, 4);
    const bHex = hex.slice(4, 6);
    const r = parseInt(rHex, 16) / 255;
    const g = parseInt(gHex, 16) / 255;
    const b = parseInt(bHex, 16) / 255;

    const rgb = [r, g, b];

    const min = Math.min(...rgb);
    const max = Math.max(...rgb);
    // console.log('min,max');
    // console.log(min, max);

    const luminance = Math.round(((min + max) / 2) * 100) / 100;
    // console.log(luminance);

    let saturation = 0;
    if (max == min) {
      // console.log('equal');
      saturation = 0;
    } else if (luminance <= 0.5) {
      // console.log('less or equal to 0.5');
      saturation = Math.round(((max - min) / (max + min)) * 100) / 100;
    } else {
      // console.log('more than 0.5');
      saturation = Math.round(((max - min) / (2.0 - max - min)) * 100) / 100;
    }
    // console.log(saturation);

    let hue = 0;
    if (max - min === 0) {
      hue = 0;
    } else if (Math.max(...rgb) === rgb[0]) {
      // console.log(`red`);
      // console.log(rgb);
      hue = Math.round(((g - b) / (max - min)) * 60);
    } else if (Math.max(...rgb) === rgb[1]) {
      // console.log('green');
      // console.log(rgb);
      hue = Math.round((2.0 + (b - r) / (max - min)) * 60);
    } else if (Math.max(...rgb) === rgb[2]) {
      // console.log('blue');
      // console.log(rgb);
      hue = Math.round((4.0 + (r - g) / (max - min)) * 60);
    }

    if (hue < 0) {
      hue = hue + 360;
    } else if (hue > 360) {
      hue = hue - 360;
    }

    // console.log([hue, saturation, luminance, hex]);
    return [hue, saturation, luminance];
  }
}
